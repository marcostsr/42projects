#include "libft.h"

/**
 * @brief Counts the number of nodes in a list.
 * @param t_list *lst
 * @return int
 */
int	ft_lstsize(t_list *lst)
{
	int	size;

	size = 0;
	while (lst)
	{
		size++;
		lst = lst->next;
	}
	return (size);
}
