#include "libft.h"

/**
 * @brief Finds the last occurrence of a character in a string.
 * @param const char *s
 * @param int c
 * @return char*
 */
char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i--;
	}
	return (0);
}
