#include "libft.h"

/**
 * @brief Applies a function to each nodeâ€™s content.
 * @param t_list *lst
 * @param void (*f)(void*)
 * @return void
 */
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		f(lst->content);
		lst = lst->next;
	}
}
