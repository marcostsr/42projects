#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	n;
	int	i;
	int	aux;

	i = 0;
	n = size / 2 ;
	while (i < n)
	{
		aux = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = aux;
		i++;
	}
}
