#include "libft.h"

/**
 * @brief Deletes one node using a delete function.
 * @param t_list *lst
 * @param void (*del)(void*)
 * @return void
 */
void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}
