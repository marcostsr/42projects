#include "libft.h"

/**
 * @brief Checks if a character is alphabetic.
 * @param int c.
 * @return int â€” nonzero if true, 0 if false
 */
int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}
