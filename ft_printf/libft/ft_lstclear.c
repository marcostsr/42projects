#include "libft.h"

/**
 * @brief Deletes all nodes and frees the list.
 * @param t_list **lst
 * @param void (*del)(void*)
 * @return void
 */
void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		temp = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = temp;
	}
}
