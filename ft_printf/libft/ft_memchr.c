#include "libft.h"

/**
 * @brief Searches for a byte in a memory block.
 * @param const void *s
 * @param int c
 * @param size_t n
 * @return void* â€” pointer to first occurrence or NULL
 */
void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*ptr;
	unsigned char		uc;
	size_t				i;

	ptr = (const unsigned char *)s;
	uc = (unsigned char)c;
	i = 0;
	while (i < n)
	{
		if (ptr[i] == uc)
			return ((void *)(ptr + i));
		i++;
	}
	return (0);
}
