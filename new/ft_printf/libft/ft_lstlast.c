#include "libft.h"

/**
 * @brief Returns the last node of a list.
 * @param t_list *lst
 * @return t_list*
 */
t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
